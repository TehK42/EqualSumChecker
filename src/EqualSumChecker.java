public class EqualSumChecker {
    //Method that checks if the first and second values are equal to the third value after combining
    public static boolean hasEqualSum(int a, int b, int c){
        //checks if the first and second values are equal to the third value after combining
        //If equal then return true
        if(a + b == c){
            return true;
        }else{
            return false;
        }
    }
}
